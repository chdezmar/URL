    # Merges the array into a parameter string of the form <tt>?key=value&foo=bar</tt>
    def to_s
      return '' if empty?
      '?' + to_a.inject(Array.new) do |ret,param|
        val = param[1].to_s
        
        val = CGI.escape(val)# if val =~ /(\/|\?|\s)/
        
        if param && val
          ret << %{#{param[0].to_s}=#{val}}
        elsif param
          ret << param[0].to_s
        end
        ret
      end.join('&')
    end
  end
